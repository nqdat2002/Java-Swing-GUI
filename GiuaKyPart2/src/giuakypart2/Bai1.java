    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        // TODO add your handling code here:
        if(evt.getSource() == jMenuItem3){
            int result = ifc.showSaveDialog(this);
            if(result == JFileChooser.APPROVE_OPTION){
                File res = ifc.getSelectedFile();
                System.out.println("Save as file: " + res.getAbsolutePath());
                try {
                    FileWriter out = new FileWriter(res.getName());
                    String s = jTextArea1.getText();
                    out.write(s);
                    out.close();
                } catch (IOException ex) {
                    Logger.getLogger(Bai1.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        }
    }//GEN-LAST:event_jMenuItem3ActionPerformed

